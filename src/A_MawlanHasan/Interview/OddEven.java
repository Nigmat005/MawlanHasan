package A_MawlanHasan.Interview;

public class OddEven {
    public static void oddEven(int num){
        if (num%2==0){
            System.out.println("Even");
        }else{
            System.out.println("Odd");
        }
    }

    public static void main(String[] args) {
        oddEven(88);
    }

}
