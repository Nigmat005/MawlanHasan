package A_MawlanHasan.Replit;

public class Gallon_to_liters {
    public static void main(String[] args) {
        double gallon=15;
        double litter=gallon*3.785;
        System.out.println(gallon+ " gallon is equal to "+litter+ "litter");

        double litters=200;
        double gallons=litters/3.785;
        System.out.println(litters+ " litters is equal to "+ (int)gallons+ " gallons" ); //Adding int makes the result int.

    }
}
