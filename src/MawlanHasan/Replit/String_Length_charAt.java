package MawlanHasan.Replit;

public class String_Length_charAt {
    /*
    using charAt method output the 3rd letter of str string then the 5th letter then the 6th letter.

use print not println.
     */
    public static void main(String[] args) {
        String str="MawlanKhotani";
        char ch1=str.charAt(2);
        char ch2=str.charAt(4);
        char ch3=str.charAt(5);
        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println(ch3);
        System.out.println("==============");
        int i=str.length();
        System.out.println(i);
    }
}
